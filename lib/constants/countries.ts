/**
 * Check if a country code falls under GDPR or equivalent data protection laws.
 * Used to filter artists from regions where scraping without consent is risky.
 *
 * @param code - ISO 3166-1 alpha-2 country code
 * @returns true if the country has GDPR or equivalent protections
 */
export function isGDPRCountry(code: string | null | undefined): boolean {
  if (!code) return false;
  return GDPR_COUNTRY_CODES.has(code.toUpperCase());
}

