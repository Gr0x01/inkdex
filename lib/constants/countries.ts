// Helper to get country name from code
export function getCountryName(code: string): string | undefined {
  return COUNTRIES.find((c) => c.code === code)?.name;
}

// Helper to get country code from name
export function getCountryCode(name: string): string | undefined {
  return COUNTRIES.find((c) => c.name.toLowerCase() === name.toLowerCase())?.code;
}

// For dropdown options
export const COUNTRY_OPTIONS = COUNTRIES.map((c) => ({
  value: c.code,
  label: c.name,
}));

// Popular countries for quick selection (shown first in some UIs)
export const POPULAR_COUNTRIES = ['US', 'GB', 'CA', 'AU', 'DE', 'FR', 'JP', 'BR', 'MX', 'IT', 'ES', 'NL'] as const;

// Set of valid country codes for O(1) lookup validation
export const VALID_COUNTRY_CODES = new Set(COUNTRIES.map((c) => c.code));

// Helper to validate country code
export function isValidCountryCode(code: string): boolean {
  return VALID_COUNTRY_CODES.has(code);
}

// Get countries sorted with popular ones first
export function getCountriesWithPopularFirst(): Country[] {
  const popular = POPULAR_COUNTRIES.map((code) =>
    COUNTRIES.find((c) => c.code === code)
  ).filter((c): c is Country => c !== undefined);

  const rest = COUNTRIES.filter((c) => !POPULAR_COUNTRIES.includes(c.code as typeof POPULAR_COUNTRIES[number]));

  return [...popular, ...rest];
}

